package com.arsath.collections.helper;

public class MapEntrySet {
	private Object key;
	private Object value;

	public MapEntrySet() {

	}

	public Object getKey() {
		return key;
	}

	public Object getValue() {
		return value;
	}

	public void setKey(Object key) {
		this.key = key;
	}

	public void setValue(Object value) {
		this.value = value;

	}
}
